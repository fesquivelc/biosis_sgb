    private void realizarLogin() {
        String login = txtLogin.getText();
        String password = new String(txtPassword.getPassword());

        if (login.trim().equals("admin") && password.equals("sgbmoquegua")) {
            Principal principal = new Principal();
            principal.setVisible(true);
            principal.setExtendedState(principal.getExtendedState() | JFrame.MAXIMIZED_BOTH);
            this.dispose();
        } else {
            JOptionPane.showConfirmDialog(this, "Usuario o contrase√±a incorrecta", "Mensaje del sistema", JOptionPane.WARNING_MESSAGE);
        }
    }
